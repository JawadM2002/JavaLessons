package lesson3LoopsStringsandArrays;

public class ForLoop {

	public static void main(String[] args) {
		for (int i = 1; i <= 10; i++) { // 1 is the starting point, the range is up to 10
			System.out.println(i); // this will print out the numbers from 1 to 10
			
		/* for(int i = 1; i <= 5; i++)
			System.out.println("Hello World"); */ // This will print Hello World 5 times 
			
		/* Syntax: 
		 * for (initialization expr; text expr; update exp){
		 * Body of loop
		 * Statement we want to execute
		 * }
		 * 
		 * The parts of For loop are: 
		 * 1. Initialization expression - used to initialise loop counter to some value, e.g. int i=1;
		 * 2. Test expression - if condition is true, body of loop will be executed and go to update expression,
		 * otherwise we exit the loop. e.g. i <= 10;
		 * 3. Update expression - expression that increments or decrements loop variable by some value, e.g. i++ 
		 */
		}

	}

}
